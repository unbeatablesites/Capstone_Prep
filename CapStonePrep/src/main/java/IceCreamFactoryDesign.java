public class IceCreamFactoryDesign {

    private int emplHours;
    private String emplFName;
    private String emplLName;
    private int vacationTime;
    private int sickDays;

    public int getEmplHours() {
        return emplHours;
    }

    public void setEmplHours(int emplHours) {
        this.emplHours = emplHours;
    }

    public String getEmplFName() {
        return emplFName;
    }

    public void setEmplFName(String emplFName) {
        this.emplFName = emplFName;
    }

    public String getEmplLName() {
        return emplLName;
    }

    public void setEmplLName(String emplLName) {
        this.emplLName = emplLName;
    }

    public int getVacationTime() {
        return vacationTime;
    }

    public void setVacationTime(int vacationTime) {
        this.vacationTime = vacationTime;
    }

    public int getSickDays() {
        return sickDays;
    }

    public void setSickDays(int sickDays) {
        this.sickDays = sickDays;
    }
}
